// ─── Language key: simpan di localStorage ─────────────────────────────────────
const LANG_KEY = "propshare_lang";

export function getCurrentLang() {
  return localStorage.getItem(LANG_KEY) ?? "id";
}

export function setCurrentLang(lang) {
  localStorage.setItem(LANG_KEY, lang);
  // Dispatch event agar semua komponen yang listen bisa update
  window.dispatchEvent(new CustomEvent("langchange", { detail: { lang } }));
}

export function t(key) {
  const lang   = getCurrentLang();
  const dict   = translations[lang] ?? translations.id;
  return dict[key] ?? translations.id[key] ?? key;
}